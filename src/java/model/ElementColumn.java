package model;

/**
 *
 * @author Dell
 */
public class ElementColumn {

    private String name;

    public ElementColumn() {
    }

    public ElementColumn(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "ElementColumn{" + "name=" + name + '}';
    }

}
